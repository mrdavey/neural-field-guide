import { createRequire } from "node:module";
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, extname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const ts = require("typescript");
const cache = new Map();

function resolveTypeScriptModule(specifier, parentFile) {
  const candidate = resolve(dirname(parentFile), specifier);
  for (const path of [candidate, `${candidate}.ts`, join(candidate, "index.ts")]) if (existsSync(path) && extname(path) === ".ts") return path;
  throw new Error(`Cannot resolve ${specifier} from ${parentFile}`);
}

function loadTypeScriptModule(file) {
  const absolute = resolve(file);
  if (cache.has(absolute)) return cache.get(absolute).exports;
  const moduleRecord = { exports: {} };
  cache.set(absolute, moduleRecord);
  const javascript = ts.transpileModule(readFileSync(absolute, "utf8"), { compilerOptions: { module: ts.ModuleKind.CommonJS, target: ts.ScriptTarget.ES2020 }, fileName: absolute }).outputText;
  const localRequire = (specifier) => specifier.startsWith(".") ? loadTypeScriptModule(resolveTypeScriptModule(specifier, absolute)) : require(specifier);
  Function("exports", "require", "module", "__filename", "__dirname", javascript)(moduleRecord.exports, localRequire, moduleRecord, absolute, dirname(absolute));
  return moduleRecord.exports;
}

const root = resolve(fileURLToPath(new URL("..", import.meta.url)));
const llm = loadTypeScriptModule(join(root, "app/course-data.ts"));
const llmGuides = loadTypeScriptModule(join(root, "app/lesson-guides/index.ts"));
const llmCode = loadTypeScriptModule(join(root, "app/code-examples.ts"));
const world = loadTypeScriptModule(join(root, "app/world-models/index.ts"));
const worldCode = loadTypeScriptModule(join(root, "app/world-models/code-examples.ts"));
const generative = loadTypeScriptModule(join(root, "app/generative/index.ts"));
const rl = loadTypeScriptModule(join(root, "app/rl/index.ts"));
const embodied = loadTypeScriptModule(join(root, "app/embodied/index.ts"));
const planned = loadTypeScriptModule(join(root, "app/research-curriculum-manifests.ts"));

const escape = (value) => String(value).replaceAll("|", "\\|").replaceAll("\n", " ");

function existingCourseSection({ title, lessons, guides, codeExamples }) {
  const rows = lessons.map((lesson, index) => {
    const guide = guides[lesson.id];
    const next = lessons[index + 1];
    const objectives = guide?.objectives?.map((objective, objectiveIndex) => `${objectiveIndex + 1}. ${objective}`).join("<br>") ?? "MISSING";
    const prerequisites = lesson.prerequisites?.length ? lesson.prerequisites.join(", ") : "—";
    const nextUse = next ? `${next.id}${next.prerequisites?.includes(lesson.id) ? " (explicit dependency)" : " (sequence reuse)"}` : "course synthesis / later courses";
    return `| ${lesson.number} | \`${escape(lesson.id)}\` | ${escape(lesson.title)} | ${escape(prerequisites)} | ${escape(objectives)} | ${escape(nextUse)} | ${lesson.lab ?? "—"} | ${codeExamples[lesson.id] ? "yes" : "—"} | ${lesson.capstone ? "yes" : "—"} |`;
  });
  return [`## ${title} — ${lessons.length} released lessons`, "", "| # | Stable ID | Lesson | Prerequisites | Exact visible objectives | Next use | Lab | Code | Capstone |", "|---:|---|---|---|---|---|---|---|---|", ...rows, ""].join("\n");
}

function plannedCourseSection(manifest) {
  const rows = manifest.lessons.map((lesson) => `| ${lesson.number} | \`${lesson.id}\` | ${lesson.title} | \`${lesson.track}\` | ${escape(lesson.reuses.join(", "))} | ${escape(lesson.build)} | ${escape(lesson.nextUse)} | ${lesson.capstone ? "yes" : "—"} |`);
  return [`## Planned ${manifest.title} — ${manifest.lessons.length} lessons`, "", `**Course outcome:** ${manifest.outcome}`, "", "| # | Planned stable ID | Lesson | Territory | Reuses | Working-system increment | Next use | Capstone |", "|---:|---|---|---|---|---|---|---|", ...rows, ""].join("\n");
}

const generated = [
  "# Curriculum Inventory",
  "",
  "Generated by `node scripts/generate-curriculum-inventory.mjs`. Do not use this inventory as a semantic grade: it exposes joins for review, while `docs/CURRICULUM_ARCHITECTURE.md` records ownership and boundary decisions.",
  "",
  `Generated: ${new Date().toISOString().slice(0, 10)}`,
  "",
  existingCourseSection({ title: "Large Language Models", lessons: llm.lessons, guides: llmGuides.lessonGuides, codeExamples: llmCode.lessonCodeExamples }),
  existingCourseSection({ title: "World Models", lessons: world.worldModelLessons, guides: world.worldModelLessonGuides, codeExamples: worldCode.worldModelCodeExamples }),
  existingCourseSection({ title: "Generative Models", lessons: generative.generativeLessons, guides: generative.generativeLessonGuides, codeExamples: generative.generativeCodeExamples }),
  existingCourseSection({ title: "Reinforcement Learning & Control", lessons: rl.rlLessons, guides: rl.rlLessonGuides, codeExamples: rl.rlCodeExamples }),
  existingCourseSection({ title: "Embodied AI", lessons: embodied.embodiedLessons, guides: embodied.embodiedLessonGuides, codeExamples: embodied.embodiedCodeExamples }),
  ...Object.values(planned.plannedCourseManifests).filter((manifest) => !["generative", "rl", "embodied"].includes(manifest.id)).map(plannedCourseSection),
].join("\n");

writeFileSync(join(root, "docs/CURRICULUM_INVENTORY.md"), generated);
console.log(`Wrote docs/CURRICULUM_INVENTORY.md with ${llm.lessons.length + world.worldModelLessons.length + generative.generativeLessons.length + rl.rlLessons.length + embodied.embodiedLessons.length} existing and ${Object.values(planned.plannedCourseManifests).filter((manifest) => !["generative", "rl", "embodied"].includes(manifest.id)).reduce((total, manifest) => total + manifest.lessons.length, 0)} planned lessons.`);
